function roundMacro(value) {
	if (!Number.isFinite(value)) {
		return 0;
	}

	if (value >= 100) {
		return Math.round(value);
	}

	return Math.round(value * 10) / 10;
}

export function searchFoods(query = '', dietFilter = []) {
	const normalizedQuery = String(query || '').trim().toLowerCase();
	const allowedDietTypes = Array.isArray(dietFilter)
		? dietFilter.map((item) => String(item).toLowerCase())
		: [];

	return FOOD_DATABASE.filter((food) => {
		const nameMatch =
			normalizedQuery.length === 0 ||
			food.name.toLowerCase().includes(normalizedQuery);

		const dietMatch =
			allowedDietTypes.length === 0 || allowedDietTypes.includes(food.dietType);

		return nameMatch && dietMatch;
	});
}

export function scaleFood(food, quantity) {
	const basePer = Number(food?.per);
	const safeQuantity = Number(quantity);
	const factor =
		Number.isFinite(basePer) && basePer > 0 && Number.isFinite(safeQuantity)
			? safeQuantity / basePer
			: 0;

	return {
		...food,
		quantity: Number.isFinite(safeQuantity) ? safeQuantity : 0,
		scaledPer: factor,
		cal: roundMacro((Number(food?.cal) || 0) * factor),
		protein: roundMacro((Number(food?.protein) || 0) * factor),
		carb: roundMacro((Number(food?.carb) || 0) * factor),
		fat: roundMacro((Number(food?.fat) || 0) * factor),
	};
}
